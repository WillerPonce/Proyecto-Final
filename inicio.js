const característicasPorPagina = 10;
let paginaActual = 1;

function mostrarCaracteristicas() {
  const inicio = (paginaActual - 1) * característicasPorPagina;
  const fin = inicio + característicasPorPagina;

  const ContenedorFilas = document.getElementById('Filas');
  ContenedorFilas.innerHTML = ''; // Limpiar contenido previo

  for (let i = inicio; i < fin && i < características.length; i++) {
    const element = características[i];
    let Filas = '<tr>';
    Filas += '<td>' + element.id + '</td>';
    Filas += '<td><img class="imagen" src="' + element.imagen + '" alt="Imagen del producto"></td>';
    Filas += '<td>' + element.Nombre + '</td>';
    Filas += '<td>' + element.Precio + '</td>';
    Filas += '<td>' + element.Marca + '</td>';
    Filas += '<td>' + element.Unidad_de_Medida + '</td>';
    Filas += '<td>' + element.Contenido + '</td>';
    Filas += '<td><button onclick="verMas(' + i + ')">Ver más</button></td>';
    Filas += '</tr>';
    ContenedorFilas.innerHTML += Filas;
  }

  // Actualizar información de la paginación
  const totalPaginas = Math.ceil(características.length / característicasPorPagina);
  document.getElementById('pagina-actual').innerText =`Página: ${paginaActual}`;
  document.getElementById('total-paginas').innerText =`Total de páginas: ${totalPaginas}`;

  // Habilitar o deshabilitar botones de navegación 
  document.getElementById('Siguiente').disabled = paginaActual === totalPaginas;
  document.getElementById('Atras').disabled = paginaActual === 1;
}

// Función para ir a la siguiente página
function siguientePagina() {
  if (paginaActual < Math.ceil(características.length / característicasPorPagina)) {
    paginaActual++;
    mostrarCaracteristicas();
  }
}

// Función para ir a la página anterior
function paginaAnterior() {
  if (paginaActual > 1) {
    paginaActual--;
    mostrarCaracteristicas();
  }
}

// Asignar las funciones a los botones
document.getElementById('Siguiente').addEventListener('click', siguientePagina);
document.getElementById('Atras').addEventListener('click', paginaAnterior);

// Mostrar las características en la primera carga de la página
mostrarCaracteristicas();
